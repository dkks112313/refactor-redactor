using System.Collections.Generic;
using System.Linq;
using System.Text.RegularExpressions;

namespace RefactorApp
{
    public class RefactorRemoveParameterController
    {
        /// <summary>
        /// Видаляє вказаний параметр зі списку параметрів усіх викликів заданого методу в межах тексту коду.
        /// </summary>
        /// <remarks>Якщо вказаний параметр не знайдено у виклику методу, виклик залишається без змін.
        /// Цей метод впливає лише на список параметрів вказаного методу і не змінює інші елементи коду.</remarks>
        /// <param name="CodeText"> Текст вхідного коду, в якому слід шукати виклики методів.</param>
        /// <param name="methodName">Назва методу, список параметрів якого буде змінено.</param>
        /// <param name="parameter">Назва параметра, який потрібно видалити зі списку параметрів методу.</param>
        /// <returns>Рядок, що містить змінений текст коду з видаленим вказаним параметром з усіх викликів заданого методу.</returns>
        public string RemoveParameter(string CodeText, string methodName, string parameter)
        {
            // Регулярний вираз для пошуку викликів методу з будь-якими параметрами.
            string pattern = $@"\b{methodName}\s*\(([^)]*)\)";

            // Використовуємо Regex.Replace для заміни кожного виклику методу, видаляючи вказаний параметр зі списку параметрів.
            return Regex.Replace(CodeText, pattern, match =>
            {
                // Отримуємо список параметрів з першої групи захоплення регулярного виразу.
                string paramList = match.Groups[1].Value;

                var parameters = SplitParameters(paramList);

                // Фільтруємо список параметрів, видаляючи всі входження вказаного параметра.
                var filtred = parameters.Where(p => !IsSameParameter(p, parameter)).ToList(); // 

                // Якщо після фільтрації список параметрів порожній, замінюємо його на порожні дужки.
                if (filtred.Count == 0)
                {
                    return match.Value.Replace("(" + paramList + ")", "()");
                }

                // Об'єднуємо відфільтрований список параметрів назад у рядок, розділений комами.
                string newParamList = string.Join(", ", filtred);

                return match.Value.Replace(paramList, newParamList);
            });
        }

        /// <summary>
        /// Розділяє список параметрів, розділених комами, на окремі рядки параметрів, правильно обробляючи вкладені
        /// дужки та кутові дужки.
        /// </summary>
        /// <remarks>Цей метод корисний для аналізу списків параметрів, у яких самі параметри можуть
        /// містити вкладені типи-генератори або типи-кортежі, гарантуючи, що коми всередині таких груп не спричинять небажаного
        /// розбиття.</remarks>
        /// <param name="paramList">Список параметрів, які потрібно розділити за допомогою ком. Може містити вкладені круглі або кутові дужки, які
        /// розглядаються як групи і не розділяються за комами всередині них.</param>
        /// <returns>Список рядків, кожен з яких відповідає одному параметру зі списку вхідних даних. Пробіли на початку та в кінці рядка
        /// видаляються з кожного параметра.</returns>
        private List<string> SplitParameters(string paramList)
        {
            var result = new List<string>(); // Змінна для збереження розділених параметрів після обробки.
            int depth = 0; // Змінна для відстеження глибини вкладеності дужок, щоб правильно обробляти коми всередині вкладених структур.
            int start = 0; // Змінна для збереження початкової позиції поточного параметра в рядку.

            // Проходимо по кожному символу в рядку параметрів, відстежуючи глибину вкладеності дужок.
            for (int i = 0; i < paramList.Length; i++)
            {
                char c = paramList[i];

                // Якщо зустрічаємо відкриваючу дужку, збільшуємо глибину вкладеності.
                if (c == '(' || c == '<')
                {
                    depth++;
                }
                // Якщо зустрічаємо закриваючу дужку, зменшуємо глибину вкладеності.
                else if (c == ')' || c == '>')
                {
                    depth--;
                }
                // Якщо зустрічаємо кому на нульовій глибині вкладеності, це означає кінець поточного параметра, і ми додаємо його до результату.
                else if (c == ',' && depth == 0)
                {
                    result.Add(paramList.Substring(start, i - start).Trim());
                    start = i + 1;
                }
            }

            // Додаємо останній параметр після останньої коми, якщо він існує.
            if (start < paramList.Length)
            {
                result.Add(paramList.Substring(start).Trim());
            }

            return result;
        }

        /// <summary>
        /// Визначає, чи збігається вказане ім'я параметра з іменем об'єкта.
        /// </summary>
        /// <param name="param">Рядок параметра, який потрібно обчислити. Пробіли на початку та в кінці ігноруються.</param>
        /// <param name="target">Ім'я цільового параметра для порівняння.</param>
        /// <returns>true, якщо ім'я, витягнуте з param, збігається з target; в іншому випадку false.</returns>
        bool IsSameParameter(string param, string target)
        {
            // Розділяємо рядок параметра на частини за пробілами і беремо останню частину як ім'я параметра, яке потрібно порівняти з цільовим ім'ям.
            var parts = param.Trim().Split(' ');
            var name = parts.Last();

            return name == target;
        }
    }
}
